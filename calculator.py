"""
calculator.py
- Defines functions used to create a simple calculator

One function per operation, in order.
"""

import math
# First example
def add(a, b): 
    return a + b

def subtract (a,b):
    return a-b

def multiply(a,b):
    return a * b

def divide(a,b):
    return a/b

def logarithm(a,b):
    math.log(a,b)

def exponent(a,b):
    math.pow(a,b)

def square_root(a):
    return math.sqrt(a)

def hypotenuse(a, b):
    return math.hypot(a,b)









